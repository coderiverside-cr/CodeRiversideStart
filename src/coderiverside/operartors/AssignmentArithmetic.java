package coderiverside.operartors;

public class AssignmentArithmetic {

    public static void main(String[] args) {
        // Asignacion y Aritmetica
        int a = 1; // asignacion (a es 1)
        int b = a + 4; // suma (b es 5)
        int c = b - 2; // resta (c es 3)
        int d = c * b; // multiplicacion (d es 15)
        int e = d / c; // division (e es 5)
        int f = d % 6; // modulo (f es 3)

        int x = 2, y = 3;
        int z = y - x * y;  // z es -3
        int w = (y - x) * y;    // w es 3
    }
}
